package org.mgnl.nicki.vaadin.base.helper;

public class ValuePair {
	private String name;
	private String value;
	
	
	public ValuePair(String name, String value) {
		super();
		this.name = name;
		this.value = value;
	}
	public String getName() {
		return name;
	}
	public String getValue() {
		return value;
	}

}
