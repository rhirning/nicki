package org.mgnl.nicki.core.helper;

public class NameValue {
	private String name;
	private String value;
	public NameValue(String name, String value) {
		super();
		this.name = name;
		this.value = value;
	}
	public String getName() {
		return name;
	}
	public String getValue() {
		return value;
	}

}
